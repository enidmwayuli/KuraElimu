<h3> Voter Rights According to the constitution </h3><br>
<p>According to the constitution of Kenya, each Voter has the following rights: <br>
(1) Every citizen is free to make political choices, which includes the right--<br>
&nbsp(a) to form, or participate in forming, a political party;<br>
&nbsp(b) to participate in the activities of, or recruit members for,a political party; or
&nbsp(c) to campaign for a political party or cause.<br>
(2) Every citizen has the right to free, fair and regular elections<br>
 based on universal suffrage and the free expression of the will of the electors for--<br>
&nbsp(a) any elective public body or office established under this Constitution; or (b) any office of any political party of which the citizen is a member.<br>
(3) Every adult citizen has the right, without unreasonable restrictions, <br>
&nbsp(a) to be registered as a voter;<br>
&nbsp(b) to vote by secret ballot in any election or referendum; and<br>
&nbsp(c) to be a candidate for public office, or office within a political<br>
 party of which the citizen is a member and, if elected, to hold office.<br>
 <br>
 <h3> Why you need to Register </h3>
1. Voting provides an opportunity to choose a leader of your choice <br>
2. Voting provides an opportunity to vie in elections. By law, only registered 
